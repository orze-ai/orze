def deep_merge(base: dict, override: dict) -> dict:
    """Recursively merge *override* into *base* (returns new dict)."""
    merged = dict(base)
    for k, v in override.items():
        if k in merged and isinstance(merged[k], dict) and isinstance(v, dict):
            merged[k] = deep_merge(merged[k], v)
        else:
            merged[k] = v
    return merged


def load_idea_config(ideas_md: str, idea_id: str) -> dict:
    """Parse ideas.md and return the YAML config block for *idea_id*."""
    text = Path(ideas_md).read_text(encoding="utf-8")
    pattern = rf"^##\\s+{re.escape(idea_id)}\\b.*?```ya?ml\\s*\\n(.*?)```"
    m = re.search(pattern, text, re.MULTILINE | re.DOTALL)
    if not m:
        raise ValueError(f"Idea {idea_id} not found in {ideas_md}")
    return yaml.safe_load(m.group(1)) or {}


# ---------------------------------------------------------------------------
# Demo training loop — linear regression on synthetic data
# ---------------------------------------------------------------------------

def _generate_data(n=200, noise=0.1, seed=42):
    """y = 3*x + 2 + noise."""
    rng = random.Random(seed)
    xs = [rng.gauss(0, 1) for _ in range(n)]
    ys = [3.0 * x + 2.0 + rng.gauss(0, noise) for x in xs]
    return xs, ys


def _train_linear(xs, ys, lr=0.01, epochs=50):
    """Gradient descent on y = w*x + b.  Returns (w, b, history)."""
    w, b = 0.0, 0.0
    n = len(xs)
    history = []
    for epoch in range(1, epochs + 1):
        # forward
        preds = [w * x + b for x in xs]
        loss = sum((p - y) ** 2 for p, y in zip(preds, ys)) / n
        # gradients
        dw = sum(2 * (p - y) * x for p, y, x in zip(preds, ys, xs)) / n
        db = sum(2 * (p - y) for p, y in zip(preds, ys)) / n
        w -= lr * dw
        b -= lr * db
        history.append(loss)
    return w, b, history


def _r2_score(ys, preds):
    mean_y = sum(ys) / len(ys)
    ss_res = sum((y - p) ** 2 for y, p in zip(ys, preds))
    ss_tot = sum((y - mean_y) ** 2 for y in ys)
    return 1.0 - ss_res / ss_tot if ss_tot > 0 else 0.0


