def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--idea-id", required=True)
    parser.add_argument("--results-dir", required=True)
    parser.add_argument("--ideas-md", required=True)
    parser.add_argument("--config", required=True, help="Path to base config YAML")
    args = parser.parse_args()

    idea_dir = Path(args.results_dir) / args.idea_id
    idea_dir.mkdir(parents=True, exist_ok=True)

    # 1. Load base config
    base_cfg = {}
    if Path(args.config).exists():
        base_cfg = yaml.safe_load(Path(args.config).read_text()) or {}

    # 2. Load idea-specific config (written by orze before launch)
    idea_cfg_path = idea_dir / "idea_config.yaml"
    idea_cfg = {}
    if idea_cfg_path.exists():
        idea_cfg = yaml.safe_load(idea_cfg_path.read_text()) or {}

    # 3. Merge: idea overrides base
    cfg = deep_merge(base_cfg, idea_cfg)
    print(f"Training {args.idea_id} with config: {cfg}")

    # 4. Extract hyperparameters (with defaults)
    lr = float(cfg.get("learning_rate", 0.01))
    epochs = int(cfg.get("epochs", 50))
    noise = float(cfg.get("noise", 0.1))
    seed = int(cfg.get("seed", 42))

    # 5. Train
    t0 = time.time()
    xs_train, ys_train = _generate_data(n=160, noise=noise, seed=seed)
    xs_val, ys_val = _generate_data(n=40, noise=noise, seed=seed + 1)

    w, b, history = _train_linear(xs_train, ys_train, lr=lr, epochs=epochs)
    training_time = time.time() - t0

    # 6. Evaluate
    val_preds = [w * x + b for x in xs_val]
    val_loss = sum((p - y) ** 2 for p, y in zip(val_preds, ys_val)) / len(ys_val)
    train_loss = history[-1] if history else float("inf")
    r2 = _r2_score(ys_val, val_preds)

    # 7. Write metrics (orze reads this to determine success/failure)
    metrics = {
        "status": "COMPLETED",
        "val_loss": round(val_loss, 6),
        "train_loss": round(train_loss, 6),
        "r2": round(r2, 6),
        "epochs": epochs,
        "training_time": round(training_time, 2),
    }
    (idea_dir / "metrics.json").write_text(json.dumps(metrics, indent=2))
    print(f"Done. Metrics: {metrics}")


if __name__ == "__main__":
    main()
